<div id="main-container">
<?php
	if (!isset($_GET['p']))
	{
		include ("./pages/404.php");
		exit();
	}
	$p = $_GET['p'];
	switch ($p)
	{
		case "home":
			include_once("./pages/home.php");
			break;
		case "login":
			include_once("./pages/login.php");
			break;
		default:
			include ("./pages/404.php");
	}
?>
</div>
